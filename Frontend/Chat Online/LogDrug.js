const show = () => {
    const signUp = document.querySelector("#sign_up");
    signUp.style.display = "block";
    document.querySelector(".layer").style.display = "block";
  };
  
  const hide = () => {
    const signUp = document.querySelector("#sign_up");
    signUp.style.display = "none";
    document.querySelector(".layer").style.display = "none";
  };